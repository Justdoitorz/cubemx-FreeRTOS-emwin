/**
  * @brief GUI主任务
  * @note 无
  * @param 无
  * @retval 无
  */
void MainTask(void)
{

	GUI_HMEM hQR;
	char QR_String[]="http://nameytg.top";
	GUI_Init();

	GUI_SetBkColor(GUI_YELLOW);
	GUI_Clear();
	hQR=GUI_QR_Create(QR_String,5,GUI_QR_ECLEVEL_H,0);
	GUI_QR_Draw(hQR,10,10);
	GUI_QR_Delete(hQR);
	
	while(1)
	{
		GUI_Delay(100);
	}
}
